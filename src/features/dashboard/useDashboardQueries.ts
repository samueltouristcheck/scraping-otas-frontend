/**
 * Fetches the Viator listing snapshot served by GET /viator/listing.
 * The snapshot is generated by `scraping/viator/listing_scraper.py`.
 * Re-fetches every 10 minutes; stale after 5 minutes.
 */
export function useViatorListingQuery() {
  return useQuery({
    queryKey: dashboardQueryKeys.viatorListing(),
    queryFn: ({ signal }) => getViatorListing(signal),
    staleTime: 5 * 60 * 1000,
    gcTime: 30 * 60 * 1000,
    refetchInterval: 10 * 60 * 1000,
    // Never block rendering — we treat the listing as an optional enrichment
    placeholderData: (previousData) => previousData,
    retry: 1,
  });
}
