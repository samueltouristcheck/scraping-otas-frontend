import { useQuery } from "@tanstack/react-query";

import {
  getAvailabilityDayDetail,
  getAvailabilityHeatmap,
  getLatestAvailability,
  getLatestPrices,
  getPriceTimeseries,
  getSources,
  getViatorListing,
} from "@/api/market";
import { dashboardQueryKeys } from "@/features/dashboard/queryKeys";

export function useSourcesQuery(tourCode: string | null) {
  return useQuery({
    queryKey: dashboardQueryKeys.sources(tourCode ?? ""),
    queryFn: ({ signal }) => getSources(tourCode ?? "", signal),
    enabled: Boolean(tourCode),
    staleTime: 10 * 60 * 1000,
    gcTime: 30 * 60 * 1000,
    placeholderData: (previousData) => previousData,
  });
}

