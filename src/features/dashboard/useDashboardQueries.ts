/** Histórico del mes calendario: no filtra por horizon_days en la API (mezcla scrapes de distintos horizontes). */
export function usePriceTrendTimeseriesQuery(tourCode: string | null, fromDate: string, toDate: string) {
  return useQuery({
    queryKey: dashboardQueryKeys.priceTimeseriesTrend(tourCode ?? "", fromDate, toDate),
    queryFn: ({ signal }) =>
      getPriceTimeseries({
        tourCode: tourCode ?? "",
        fromDate,
        toDate,
        limit: 50_000,
        signal,
      }),
    enabled: Boolean(tourCode) && Boolean(fromDate) && Boolean(toDate) && fromDate <= toDate,
    staleTime: 2 * 60 * 1000,
    gcTime: 15 * 60 * 1000,
    placeholderData: (previousData) => previousData,
  });
}

